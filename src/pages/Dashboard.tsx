export default function Dashboard() {
  const { user, isLoaded } = useUser()
  
  if (!isLoaded) {
    return (
      <div className="min-h-screen bg-background">
        <Navigation />
        <div className="flex items-center justify-center min-h-[400px]">
          <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-primary"></div>
        </div>
      </div>
    )
  }
  
  const role = user?.unsafeMetadata?.role as string || 'fashion_enthusiast'
  const userName = user?.firstName || user?.emailAddresses[0]?.emailAddress || 'User'
  
  const getRoleDisplay = (role: string) => {
    const roleMap: Record<string, string> = {
      fashion_enthusiast: 'Fashion Enthusiast',
      designer: 'Designer',
      event_organizer: 'Event Organizer', 
      venue_owner: 'Venue Owner',
      admin: 'Administrator'
    }
    return roleMap[role] || 'Fashion Enthusiast'
  }
  
  const renderDashboard = () => {
    switch(role) {
      case 'designer':
        return <DesignerDashboard />
      case 'event_organizer':
        return <OrganizerDashboard />
      case 'venue_owner':
        return <VenueDashboard />
      case 'admin':
        return <AdminDashboard />
      default:
        return <CustomerDashboard />
    }
  }
  
  return (
    <div className="min-h-screen bg-background">
      <Navigation />
      <main className="container mx-auto px-4 py-8">
        <div className="flex items-center justify-between mb-8">
          <div>
            <h1 className="text-3xl font-playfair font-bold text-foreground mb-2">
              Welcome back, {userName}!
            </h1>
            <div className="flex items-center gap-2">
              <Badge variant="secondary">{getRoleDisplay(role)}</Badge>
              <Button variant="outline" size="sm" asChild>
                <Link to="/settings">
                  <Settings className="w-4 h-4 mr-2" />
                  Settings
                </Link>
              </Button>
            </div>
          </div>
        </div>
        
        {renderDashboard()}
      </main>
    </div>
  )
}